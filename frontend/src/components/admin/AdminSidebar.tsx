// Items que dependen de un feature flag para mostrarse.
const FLAG_POR_HREF: Record<string, string> = {
  '/admin/instructores': 'instructores',
};

export const AdminSidebar: React.FC<AdminSidebarProps> = ({ isOpen, onClose }) => {
  const pathname = usePathname();
  const { flags } = useFeatureFlags();

  const isActive = (href: string) => {
    if (href === '/admin') {
      return pathname === '/admin';
    }
    return pathname?.startsWith(href);
  };

  // Oculta los items cuyo feature flag esté apagado (p. ej. Instructores).
  const visibleItems = navItems.filter((item) => {
    const flag = FLAG_POR_HREF[item.href];
    return !flag || flags[flag];
  });

  return (
    <>
      {/* Backdrop for mobile */}
      {isOpen && (
        <div className={styles.backdrop} onClick={onClose} aria-hidden="true" />
      )}

      {/* Sidebar */}
      <aside className={`${styles.sidebar} ${isOpen ? styles.open : ''}`}>
        <nav className={styles.nav}>
          <ul className={styles.navList}>
            {visibleItems.map((item) => (
              <li key={item.href}>
                <Link
                  href={item.href}
                  className={`${styles.navItem} ${isActive(item.href) ? styles.active : ''}`}
                  onClick={onClose}
                >
                  <span className={styles.navIcon}>{item.icon}</span>
                  <span className={styles.navLabel}>{item.label}</span>
                </Link>
              </li>
            ))}
          </ul>
        </nav>

        {/* Footer Info */}
        <div className={styles.footer}>
          <div className={styles.footerInfo}>
            <svg
              xmlns="http://www.w3.org/2000/svg"
              fill="none"
              viewBox="0 0 24 24"
              strokeWidth={1.5}
              stroke="currentColor"
              className={styles.footerIcon}
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                d="M11.25 11.25l.041-.02a.75.75 0 011.063.852l-.708 2.836a.75.75 0 001.063.853l.041-.021M21 12a9 9 0 11-18 0 9 9 0 0118 0zm-9-3.75h.008v.008H12V8.25z"
              />
            </svg>
            <div className={styles.footerText}>
              <span className={styles.footerTitle}>Panel Admin</span>
              <span className={styles.footerVersion}>v1.0.0</span>
            </div>
          </div>
        </div>
      </aside>
    </>
  );
};
